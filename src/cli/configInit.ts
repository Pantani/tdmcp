import { randomBytes } from "node:crypto";
import {
  chmodSync,
  existsSync,
  lstatSync,
  mkdirSync,
  renameSync,
  unlinkSync,
  writeFileSync,
} from "node:fs";
import { homedir } from "node:os";
import { dirname, resolve } from "node:path";

/**
 * Result of {@link runConfigInit}: stdout/stderr text and a process exit code,
 * mirroring the rest of the CLI dispatcher. Code 0 = wrote a file (or would in
 * --dry-run); 1 = file existed and `force` was false; 2 = bad input.
 */
export interface ConfigInitResult {
  stdout: string;
  stderr: string;
  code: number;
  path: string;
}

export interface ConfigInitOptions {
  /** Target file path. Defaults to `~/.tdmcp/config.env`. A leading `~`, `~/`, or `~\` is expanded to the user's home directory. */
  out?: string;
  /** Overwrite an existing file. Without it, refuses (code 1) if the path exists. */
  force?: boolean;
  /** When true, returns the body that would be written without touching the filesystem. */
  dryRun?: boolean;
  /** Shared TD bridge token to bake into the starter file as an uncommented `TDMCP_BRIDGE_TOKEN=...` line. */
  bridgeToken?: string;
}

/** Expand a leading `~`, `~/`, or `~\\` in a path to the user's home directory. */
function expandHome(p: string): string {
  if (p === "~") return resolve(homedir());
  if (p.startsWith("~/") || p.startsWith("~\\")) return resolve(homedir(), p.slice(2));
  return resolve(p);
}

